//payment
export const brainTreePaymentController = async (req, res) => {
  try {
    const { nonce, cart } = req.body;
    let total = 0;
    cart.forEach((item) => {
      total += item.price;
    });

    gateway.transaction.sale(
      {
        amount: total,
        paymentMethodNonce: nonce,
        options: {
          submitForSettlement: true,
        },
      },
      async (error, result) => {
        if (result) {
          const order = await new orderModel({
            products: cart,
            payment: result,
            buyer: req.user._id,
          }).save();

          // Check for PDFs in the ordered products
          const orderedProducts = await productModel.find({
            _id: { $in: cart.map(item => item._id) }
          }).select("pdf");

          // Create an array of download links for PDFs
          const pdfLinks = orderedProducts.map(product => {
            if (product.pdf.data) {
              return `/api/product-pdf/${product._id}`;
            }
            return null;
          }).filter(link => link !== null);

          res.json({ ok: true, pdfLinks });
        } else {
          res.status(500).send(error);
        }
      }
    );
  } catch (error) {
    console.log(error);
    res.status(500).send({
      success: false,
      message: "Error processing payment",
      error,
    });
  }
};

